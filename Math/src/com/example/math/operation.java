package com.example.math;

public class operation {
	
	public static double addition(double number_one,double number_two){
		double result=number_one+number_two;
		return result;
	}
	public static double subtraction(double number_one,double number_two){
		double result=number_one-number_two;
		return result;
	}
	public static double division(double number_one,double number_two){
		double result=number_one/number_two;
		return result;

}
	public static double multiplication(double number_one,double number_two){
		double result=number_one*number_two;
		return result;
	}
}